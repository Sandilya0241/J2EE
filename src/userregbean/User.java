package userregbean;

public class User {
	private String strUserName, strUserEMail, strPasswd;

	public String getStrUserName() {
		return strUserName;
	}

	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}

	public String getStrUserEMail() {
		return strUserEMail;
	}

	public void setStrUserEMail(String strUserEMail) {
		this.strUserEMail = strUserEMail;
	}

	public String getStrPasswd() {
		return strPasswd;
	}

	public void setStrPasswd(String strPasswd) {
		this.strPasswd = strPasswd;
	}
	
}
